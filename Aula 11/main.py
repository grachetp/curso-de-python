lista = [1, 2, 3, 4, 'Pedrinho', 1.3, 10.5]

print(lista[1:4]) #executa do 1 ao 3 (4 não é exibido)
print(lista[:4]) #executa do 0 ao 3 (4 não é exibido)
print(lista[2:]) #executa do 6 ao 3 (2 não é exibido)
print(lista[-1])

